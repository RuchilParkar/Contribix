// Returns a 7x53 grid (7 rows, 53 columns) filled with levels (0 to 4)
export type DesignGrid = number[][]; // 7 rows, 53 cols

export function createEmptyGrid(): DesignGrid {
  return Array(7).fill(0).map(() => Array(53).fill(0));
}

// Convert text to pixel values placed in the grid
export function convertTextToGrid(text: string): DesignGrid {
  const grid = createEmptyGrid();
  const upperText = text.toUpperCase();
  
  // Track offset column index. Each char is 5 pixels wide + 1 space column
  let colOffset = 2; // start with a small margin
  
  for (let i = 0; i < upperText.length; i++) {
    const char = upperText[i] || ' ';
    const fontChar = PIXEL_FONT[char] || PIXEL_FONT[' '];
    
    if (colOffset + 5 >= 53) break; // grid width exceeded
    
    // Draw font matrix onto grid
    for (let r = 0; r < 7; r++) {
      for (let c = 0; c < 5; c++) {
        if (fontChar[r]?.[c] === 1) {
          grid[r]![colOffset + c] = 4; // Max intensity level
        }
      }
    }
    colOffset += 6; // advance char width + spacing
  }
  
  return grid;
}

