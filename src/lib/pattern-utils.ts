// Calculate dates and total commits needed to generate this pattern on a GitHub calendar
export interface CommitEstimation {
  totalCommits: number;
  activeDaysCount: number;
  commitsPerLevel: Record<number, number>; // level: commits required
  details: { date: string; weekday: number; weekIndex: number; commits: number }[];
}

export function estimateCommitRequirements(grid: DesignGrid, intensity: number = 4): CommitEstimation {
  // A standard contribution map has 53 columns representing weeks, and 7 rows representing days (Sunday to Saturday)
  // We need to map grid indices (r, c) to dates.
  // The grid[0][0] corresponds to the first Sunday of the 365-day range.
  // We back-calculate from today. Let's find the start date (364 days ago, aligned to the starting Sunday).
  const now = new Date();
  const currentDayOfWeek = now.getDay();
  const startDate = new Date(now.getTime() - (364 + currentDayOfWeek) * 24 * 60 * 60 * 1000);
  
  let totalCommits = 0;
  let activeDaysCount = 0;
  const details: CommitEstimation['details'] = [];
  
  const commitsPerLevel: Record<number, number> = {
    0: 0,
    1: 1,
    2: 3,
    3: 6,
    4: 10,
  };

  for (let c = 0; c < 53; c++) {
    for (let r = 0; r < 7; r++) {
      const level = grid[r]?.[c] || 0;
      if (level > 0) {
        const requiredCommits = commitsPerLevel[level] !== undefined 
          ? (commitsPerLevel[level] as number) * intensity 
          : level * intensity;
        
        // Find the date for this column/row
        const daysOffset = c * 7 + r;
        const targetDate = new Date(startDate.getTime() + daysOffset * 24 * 60 * 60 * 1000);
        const dateStr = targetDate.toISOString().split('T')[0] || '';
        
        totalCommits += requiredCommits;
        activeDaysCount++;
        
        details.push({
          date: dateStr,
          weekday: r,
          weekIndex: c,
          commits: requiredCommits,
        });
      }
    }
  }

  return {
    totalCommits,
    activeDaysCount,
    commitsPerLevel,
    details,
  };
}

// Generate the script content for downloading
export function generateCommitBashScript(estimation: CommitEstimation, repoName: string = 'contribix-art'): string {
  let scriptContent = `#!/bin/bash
# Contribix Heatmap Studio - Automated Commit Script
# Generated on: ${new Date().toLocaleDateString()}

REPO_NAME="${repoName}"

echo "🎨 Initializing temporary Git repository: $REPO_NAME"
mkdir -p "$REPO_NAME"
cd "$REPO_NAME"
git init

# Configure local git values for this temporary repo
git config user.name "Contribix Designer"
git config user.email "designer@contribix.io"

echo "✏️ Creating dummy files and writing commit structures..."
touch README.md
echo "# Contribix Heatmap Art" > README.md
git add README.md
git commit -m "Initial repository setup"

`;

  // Append commits for each target date
  estimation.details.forEach(({ date, commits }) => {
    // Generate dates with timestamps
    scriptContent += `# Backdated commits for ${date}\n`;
    for (let i = 0; i < commits; i++) {
      // Offset times a bit to make it natural (e.g. 10:00:00, 10:01:00, etc)
      const hour = 10 + Math.floor(i / 60);
      const minute = i % 60;
      const timeStr = `${hour < 10 ? '0' + hour : hour}:${minute < 10 ? '0' + minute : minute}:00`;
      const dateString = `${date}T${timeStr}`;
      
      scriptContent += `echo "Art log: ${dateString}-${i}" >> art-log.txt\n`;
      scriptContent += `git add art-log.txt\n`;
      scriptContent += `GIT_AUTHOR_DATE="${dateString}" GIT_COMMITTER_DATE="${dateString}" git commit -m "Contribix artwork commit ${i + 1} for ${date}"\n`;
    }
    scriptContent += `\n`;
  });

  scriptContent += `echo "✅ Completed generating all ${estimation.totalCommits} backdated commits across ${estimation.activeDaysCount} days!"
echo ""
echo "👉 Instructions to apply this on your GitHub calendar:"
echo "1. Create a NEW blank repository on GitHub named '$REPO_NAME'."
echo "2. Run: git remote add origin https://github.com/YOUR_GITHUB_USERNAME/$REPO_NAME.git"
echo "3. Run: git branch -M main"
echo "4. Run: git push -u origin main -f"
echo "⚠️  WARNING: Use this script ONLY in a new, separate repository. Never push backdated commits to a production project repository."
`;

  return scriptContent;
}
